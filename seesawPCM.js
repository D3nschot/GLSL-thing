const offset = angleData[0]; // start from 0
const N = angleData.length;
const T_total = t_end - t_start;
const dt = T_total / (N - 1); // time step between samples

function getAnglePCM(t) {
  const tClamped = Math.max(t_start, Math.min(t_end, t)); // clamp to range
  const pos = (tClamped - t_start) / dt; // floating-point index
  const i = Math.floor(pos);
  const frac = pos - i;

  const a = (angleData[i]   ?? angleData[N - 1]) - offset;
  const b = (angleData[i+1] ?? angleData[N - 1]) - offset;

  return (a + frac * (b - a)) + angleData[0]
}